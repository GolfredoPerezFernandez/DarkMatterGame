import { useMoralis } from 'react-moralis';
import { useEffect, useState } from 'react';
const Header = (props:any) => {
  const { user, isWeb3Enabled, enableWeb3,Moralis, isAuthenticated, isWeb3EnableLoading } = useMoralis();
  const [balance, setBalance] = useState('');
useEffect(()=>{
  
  async function init2(){
    console.log('habilitando1'+isWeb3Enabled)
    if(!isWeb3Enabled){
console.log('habilitando')
      await enableWeb3();
    } 
  }
  init2()
},[isWeb3Enabled])
  useEffect(() => {
    async function init() {
      
      const chainId = Moralis.getChainId();
      const sendOptionsSymbol3 = {
        contractAddress: '0x7cf12057804499A21A08c40B87E3DE9Ff237E566',
        functionName: 'balanceOf',
        abi: erc20ABI,
        params: {
          account: user?.get('ethAddress'), 
        },
      };
      const ownerOf = await Moralis.executeFunction(sendOptionsSymbol3);

      setBalance(Math.round(parseFloat(Moralis.Units.FromWei(ownerOf.toString()))).toString());
    }
    if (isWeb3Enabled && isAuthenticated && !isWeb3EnableLoading && user) {
      init();
    }
  }, [user, isWeb3Enabled, isAuthenticated, isWeb3EnableLoading]);
  return (
    <Box borderBottom="1px" borderBottomColor="chakra-border-color">
      <Container maxW="container.xl" p={'10px'}>
        <Flex align="center" justify="space-between">
          <MoralisLogo />
          <NavBar />
          <HStack gap={'10px'}>  
            {!user ? null : props.width < 800 ? null : (
              <Text fontSize="1xl" marginLeft={20} textAlign={'right'}>
                {balance === '' ? '' : balance.concat(' DKMT')}
              </Text>
            )}
            <ConnectButton />
          </HStack>
        </Flex>
      </Container>
    </Box>
  );
};

